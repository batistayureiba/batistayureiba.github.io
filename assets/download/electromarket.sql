BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "categoria" (
	"catCodigo"	INTEGER,
	"catNombre"	TEXT NOT NULL UNIQUE,
	PRIMARY KEY("catCodigo" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "cliente" (
	"cteCodigo"	INTEGER,
	"cteNombre"	TEXT NOT NULL,
	"cteApellido"	TEXT NOT NULL,
	"cteEmail"	TEXT NOT NULL UNIQUE,
	"cteFechaRegistro"	TEXT NOT NULL,
	"CteDOB"	TEXT NOT NULL,
	PRIMARY KEY("cteCodigo" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "producto" (
	"proCodigo"	INTEGER NOT NULL,
	"proNombre"	TEXT NOT NULL UNIQUE,
	"catCodigo"	INTEGER NOT NULL,
	"proPrecio"	REAL NOT NULL,
	"proCosto"	REAL NOT NULL,
	"proStock"	INTEGER NOT NULL DEFAULT 0,
	"proActivo"	INTEGER NOT NULL,
	"proParaLaVenta"	INTEGER NOT NULL,
	PRIMARY KEY("proCodigo" AUTOINCREMENT),
	FOREIGN KEY("catCodigo") REFERENCES "categoria"("catCodigo")
);
CREATE TABLE IF NOT EXISTS "tienda" (
	"tieCodigo"	INTEGER,
	"tieNombre"	TEXT UNIQUE,
	"tieCiudad"	TEXT NOT NULL,
	"tieRegion"	TEXT NOT NULL,
	PRIMARY KEY("tieCodigo" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "venta" (
	"vtaCodigo"	INTEGER,
	"cteCodigo"	INTEGER NOT NULL,
	"tieCodigo"	INTEGER NOT NULL,
	"vtaFecha"	TEXT NOT NULL,
	"vtaHora"	TEXT NOT NULL,
	"vtaTotal"	REAL NOT NULL,
	PRIMARY KEY("vtaCodigo" AUTOINCREMENT),
	FOREIGN KEY("cteCodigo") REFERENCES "cliente"("cteCodigo"),
	FOREIGN KEY("tieCodigo") REFERENCES "tienda"("tieCodigo")
);
CREATE TABLE IF NOT EXISTS "venta_detalle" (
	"detVtaCodigo"	INTEGER,
	"vtaCodigo"	INTEGER NOT NULL,
	"proCodigo"	INTEGER NOT NULL,
	"unidad"	INTEGER NOT NULL,
	"precio_unitario"	REAL NOT NULL,
	"subtotal"	REAL NOT NULL,
	PRIMARY KEY("detVtaCodigo" AUTOINCREMENT),
	FOREIGN KEY("proCodigo") REFERENCES "producto"("proCodigo"),
	FOREIGN KEY("vtaCodigo") REFERENCES "venta"("vtaCodigo")
);
