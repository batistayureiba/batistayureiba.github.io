CREATE VIEW VistaDesviacionEstandar AS
SELECT 
    ROUND(AVG(p.proPrecio), 2) AS promedio,
    ROUND(SQRT(AVG((p.proPrecio - (
        SELECT AVG(vd.precio_unitario)
        FROM venta_detalle AS vd
    )) * (p.proPrecio - (
        SELECT AVG(vd.precio_unitario)
        FROM venta_detalle AS vd
    )))), 2) AS desviacion_estandar
FROM 
	producto AS p;
CREATE VIEW VistaGastoPromedioPorCliente AS
SELECT 
	c.cteNombre AS Nombre_Cliente, 
	c.cteApellido AS Apellido_Cliente,
    ROUND(AVG(v.vtaTotal),2) AS GastoPromedio,
    COUNT(v.vtaCodigo) AS Frecuencia,
    MAX(v.vtaFecha) AS UltimaFechadeCompra
FROM 
	venta AS v
INNER JOIN 
	venta_detalle AS vd ON v.vtaCodigo = vd.vtaCodigo
LEFT JOIN 
	cliente AS c ON v.cteCodigo = c.cteCodigo
GROUP BY 
	v.cteCodigo
ORDER BY 
	GastoPromedio DESC
LIMIT 10;
